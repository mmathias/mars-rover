public class Plateau {
    private int limitX;
    private int limitY;

    public Plateau(int limitX, int limitY) {
        this.limitX = limitX;
        this.limitY = limitY;
    }

    public int getLimitX() {
        return limitX;
    }

    public void setLimitX(int limitX) {
        this.limitX = limitX;
    }

    public int getLimitY() {
        return limitY;
    }

    public void setLimitY(int limitY) {
        this.limitY = limitY;
    }
}
